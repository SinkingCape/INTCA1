#include <cstdio>
#include <stdio.h>

int main() {
    // Initialize variables a, b, and c
    int a = 10;
    int b = 20;
    int c = 30;
    // Add values of a and b, store it in a new variable d
    int d = a + b;
    printf("Value of d (a+b); %d\n",d);

    // Subtract variable c from d, store in a new variable e
    int e = d - c;
    printf("Value of e (d-c); %d\n",e);

    return 0;
}

